#!/bin/sh
set -e

#--------------------------------------------------------------------------------------------------
# Settings
#--------------------------------------------------------------------------------------------------

thirdparty="../3rdparty"

#--------------------------------------------------------------------------------------------------

compiler_win="mingw"

qt="qt5"

#--------------------------------------------------------------------------------------------------
# Functions
#--------------------------------------------------------------------------------------------------

replace()
{
    expression='s/'"$1"'=\"'"$2"'"/'"$1"'=\"'"$3"'"/g'

    sed -i $expression environment.sh

    sed -i $expression configure.sh
    sed -i $expression build.sh
    sed -i $expression deploy.sh
}

#--------------------------------------------------------------------------------------------------
# Syntax
#--------------------------------------------------------------------------------------------------

if [ $# != 2 -a $# != 3 ] \
   || \
   [ $1 != "mingw" -a $1 != "msvc" ] || [ $2 != "qt4" -a $2 != "qt5" ] \
   || \
   [ $# = 3 -a "$3" != "sky" ]; then

    echo "Usage: environment <mingw | msvc> <qt4 | qt5> [3rdparty]"

    exit 1
fi

#--------------------------------------------------------------------------------------------------
# 3rdparty
#--------------------------------------------------------------------------------------------------

if [ "$2" = "sky" ]; then

    echo "SET ENVIRONMENT 3rdparty"
    echo "------------------------"

    cd "$thirdparty"

    sh environment.sh $1 $2

    cd -

    echo "------------------------"
    echo ""
fi

#--------------------------------------------------------------------------------------------------
# Replacements
#--------------------------------------------------------------------------------------------------

replace compiler_win $compiler_win $1
replace qt           $qt           $2
