#!/bin/sh
set -e

#--------------------------------------------------------------------------------------------------
# Settings
#--------------------------------------------------------------------------------------------------

source="https://github.com/omega-gg/3rdparty"

external=".."

#--------------------------------------------------------------------------------------------------
# Syntax
#--------------------------------------------------------------------------------------------------

if [ $# != 1 ] || [ $1 != "win32" -a $1 != "win64" -a $1 != "macOS" -a $1 != "linux"     -a \
                                                                       $1 != "android32" -a \
                                                                       $1 != "android64" ]; then

    echo "Usage: 3rdparty <win32 | win64 | macOS | linux | android32 | android64>"

    exit 1
fi

#--------------------------------------------------------------------------------------------------
# Clone
#--------------------------------------------------------------------------------------------------

if [ ! -d "3rdparty" ]; then

    git clone $source
fi

#--------------------------------------------------------------------------------------------------
# Generate
#--------------------------------------------------------------------------------------------------

cd 3rdparty

sh generate.sh $1
